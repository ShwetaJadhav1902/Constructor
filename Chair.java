package com.constructor;

public class Chair {
	private int chairPrice;
	private String chairColor;
	private String chairWaranty;
	public Chair() {
		super();
	}
	public int getChairPrice() {
		return chairPrice;
	}
	public void setChairPrice(int chairPrice) {
		this.chairPrice = chairPrice;
	}
	public String getChairColor() {
		return chairColor;
	}
	public void setChairColor(String chairColor) {
		this.chairColor = chairColor;
	}
	public String getChairWaranty() {
		return chairWaranty;
	}
	public void setChairWaranty(String chairWaranty) {
		this.chairWaranty = chairWaranty;
	}
	
	

}
